import java.util.*;

public class Main {
    public static void main(String[] args) {
        int sum =0;
        
        for(int i=0; i<=10; i++){
          sum+=i;
        }
        System.out.print(sum);
        
        // while loop. 
        
        int i=0;
        int sum=0;
        while(i<=10){
          sum = sum+i;
          i++;
        }
        System.out.print(sum);
  }
}